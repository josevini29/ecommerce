   public void validate(ServletRequest request) throws Exception {
       String id = request.getParameter("id_produto");
       String nome = request.getParameter("nm_produto");
       String descricao = request.getParameter("ds_produto");
       String valor = request.getParameter("vl_produto");
       request.setAttribute("id_produto", id==null?"":id);
       request.setAttribute("nm_produto", nome==null?"":nome);
       request.setAttribute("ds_produto", descricao==null?"":descricao);
       request.setAttribute("vl_produto", valor==null?"":valor);

       if (nome == null || nome.equals("")) {
           throw new Exception("Nome do produto é Obrigátorio!");
       }
       if (descricao == null || descricao.equals("")) {
           throw new Exception("Descrição do Produto é Obrigátoria!");
       }
       if (valor == null || valor.equals("")) {
           throw new Exception("Valor do Produto é Obrigátorio!");
       }
       try{
           Double.parseDouble(valor);
       } catch(Exception ex){
           throw new Exception("Valor do Produto é inválido!");
       }

   }

   public void doFilter(ServletRequest request, ServletResponse response,
           FilterChain chain)
           throws IOException, ServletException {

       try {
           validate(request);
           chain.doFilter(request, response);
       } catch (Exception e) {
           request.setAttribute("errMsg", e.getMessage());
           RequestDispatcher rd = request.getRequestDispatcher("cadastro.jsp");
           rd.forward(request, response);
       }

   }
    
